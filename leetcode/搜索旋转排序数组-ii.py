class Solution:
    def minNumberInRotateArray(self, rotateArray):
        # write code here
        if not rotateArray or len(rotateArray) == 0:
            return 0
        low = 0
        high = len(rotateArray) - 1
        
        while low < high:
            mid = (low + high) // 2
            if rotateArray[mid] > rotateArray[high]:
                low = mid + 1
            elif rotateArray[mid] < rotateArray[high]:
                high = mid
            else:
                high -= 1
        return low
 
